MANGLED_NUMBERS = []

SEARCH_PATTERN = r'(\d{3})-(\d{3})-(\d{4})'

REPLACE_PATTERNS = [
    r'(\1) \2-\3', r'\1/\2.\3', r'\1\2\3', r'\1.\2.\3', r'\1-\2-\3'
]

for item in PHONE_NUMBERS:
    replace_pattern = random.choice(REPLACE_PATTERNS)
    mangled_number = re.sub(SEARCH_PATTERN, replace_pattern, item)
    MANGLED_NUMBERS.append(mangled_number)

